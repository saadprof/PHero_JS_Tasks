/*
    Arithmatic Operators:
        i. Addition: Binary plus sign '+' with two operands perform addition.
            i.e -> 2 + 3
        ii. Subtraction: Binary minus  sign '-' with two operands perform subtraction from the first value
            i.e -> 10 - 5
        iii. Multiplication: Binary multiply sign '*' with two operands perform multiplication.
            i.e -> 5 * 5
        iv. Division: Binary division sign '/' performs division.
            i.e -> 10 / 5
        v. Modulas/Remainder: Modulas or Remainder sign returns the remainder after division. 
*/ 

// Addition Example:
let add = 2 + 3;
console.log(add);
console.log('------------------------------------');

// Subtraction Example:
let subtract = 10 - 5;
console.log(subtract);
console.log('------------------------------------');

// Multiplication Example:
let multiply = 5 * 5;
console.log(multiply);
console.log('------------------------------------');

// Division Example:
let devide = 10 / 5 ;
console.log(devide);
console.log('------------------------------------');

// Remainder Example:
let remainder = 50 % 9;
console.log(remainder);
console.log('------------------------------------');


// Arithmatic Shorthands do the operations with itself
// Addition
let addShort = 5;
addShort += 5
console.log(addShort);
console.log('------------------------------------');


// Subtraction
let subShort = 20;
subShort -= 15;
console.log(subShort);
console.log('------------------------------------');

// Multiplication
let multiplyShort = 5;
multiplyShort *= 5;
console.log(multiplyShort);
console.log('------------------------------------');

// Division
let divideShort = 120;
divideShort /= 10;
console.log(divideShort);
console.log('------------------------------------');

// Remainder
let remainderShort = 100;
remainderShort %= 9;
console.log(remainderShort);
console.log('------------------------------------');
